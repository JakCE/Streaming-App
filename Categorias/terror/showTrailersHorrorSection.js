function openTRAILER()
{
    const btnCloseTrailer = document.querySelector("#close-trailer-btn");
    const trailerpreview = document.querySelector("#trailer-preview");
    const  trailerpreviewEsc = document.getElementById("trailerpreviewid")


    trailerpreview.showModal();
    
        function press_Esc(){
            tecla_Esc = event.keyCode;
        
            if(tecla_Esc== 27)
            {
                trailerpreviewEsc.pause();
            } 
        }
        
        window.onkeydown = press_Esc;

        btnCloseTrailer.addEventListener("click", ()=>{
        trailerpreview.close();
        trailerpreviewEsc.pause();
    })
}

